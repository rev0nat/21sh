#include <stdlib.h>
#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	char	*r;
	size_t	l1;
	size_t	l2;

	if (s1 == NULL || s2 == NULL)
		return (NULL);
	l1 = ft_strlen(s1);
	l2 = ft_strlen(s2);
	if (NULL == (r = malloc(l1 + l2 + 1)))
		return (NULL);
	ft_memcpy(r, s1, l1);
	ft_memcpy(r + l1, s2, l2);
	r[l1 + l2] = '\0';
	return (r);
}
