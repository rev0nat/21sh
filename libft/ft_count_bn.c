#include "libft.h"

size_t	ft_count_bn(char *str)
{
	int i;
	int bn;

	i = 0;
	bn = 0;
	if (!str)
		return (bn);
	while (str[i])
	{
		if (str[i] == '\n')
			bn++;
		i++;
	}
	return (bn);
}
