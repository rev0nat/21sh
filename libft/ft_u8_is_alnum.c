#include <libft.h>

int	ft_u8_is_alnum(uint8_t c)
{
	return ((c >= '0' && c <= '9')
			|| (c >= 'A' && c <= 'Z')
			|| (c >= 'a' && c <= 'z')
			|| (c >= 192 && c <= 253));
}
